
//  Create a program to find largest of two numbers. */
import java.util.*;

public class FindLargeNum {
    public static void main(String[] args) {
        // take a input from the user types and variable
        Scanner sc = new Scanner(system.in);
        System.out.println(" enter the two numbers");
        int a = sc.nextInt();
        int b = sc.nextInt();
        // check the condition and print
        if (a > b) {
            System.out.println("is this a largest number");
        } else {
            System.out.println("lowest");
        }

    }
}